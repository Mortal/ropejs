function fib_generic(n, fib0, fib1, op) {
  if (n <= 0) return fib0;
  if (n == 1) return fib1;
  var a = fib0, p = fib1, r;
  for (var i = 2; i <= n; ++i) {
    r = op(a, p);
    a = p;
    p = r;
  }
  return r;
}
